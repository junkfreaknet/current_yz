    private void common_action_btn_ok(){
        this.setParaFromForm();
        this.setVisible(false);
    }
    private void setParaFromForm(){
        
        String product_name;
        String buff;
        
        buff=(String)this.cmbbx_product_name.getSelectedItem();        
        product_name=buff.toLowerCase(); 
        //
        this.para.setForNameString(this.txt_for_name_string.getText());
        this.para.setNamDataBase(this.txt_database_name.getText());       
        this.para.setNameHost(this.txt_host.getText());
        this.para.setNameProduct((String)this.cmbbx_product_name.getSelectedItem());        
        this.para.setNameServerInstance(this.txt_server_instance.getText());
        this.para.setPassWord(this.txt_pass_word.getText());
        this.para.setPort(this.txt_port.getText());
        this.para.setUser(this.txt_user_name.getText());
        //
        this.para_ms.setForNameString(this.txt_for_name_string.getText());
        this.para_ms.setNamDataBase(this.txt_database_name.getText());       
        this.para_ms.setNameHost(this.txt_host.getText());
        this.para_ms.setNameProduct((String)this.cmbbx_product_name.getSelectedItem());        
        this.para_ms.setNameServerInstance(this.txt_server_instance.getText());
        this.para_ms.setPassWord(this.txt_pass_word.getText());
        this.para_ms.setPort(this.txt_port.getText());
        this.para_ms.setUser(this.txt_user_name.getText());
        
        //
        if(product_name.equals(ConstantsDB.MS_SQLSERVER_PRODUCT_NAME)){
            this.rv=this.para_ms;
        }
        if(product_name.equals(ConstantsDB.MySQL_PRODUCT_NAME)){
            this.rv=this.para;
        }
                
    }
    private void common_action_btn_connect(){
     
        String message;
        JLabel label=new JLabel();
        
        //this.common_action_btn_ok();
        this.setParaFromForm();
        if(my.db.utilities.Utilities.isEnableServer(rv)){
            //when success
            label.setText("connecting db server is OK.");
            label.setForeground(Color.BLACK);
        }else{
            //when failed
            label.setText("connecting db server is NG.");             
            label.setForeground(Color.RED);
        }
        
        JOptionPane.showMessageDialog(this, label);
        
    }
    private void common_action_btn_cancel(){
        rv=null;
        this.setVisible(false);
    }

    private void common_action_form_component_shown(){
        
        //set product names
        this.cmbbx_product_name.removeAllItems();
        this.cmbbx_product_name.addItem(ConstantsDB.MS_SQLSERVER_PRODUCT_NAME);
        this.cmbbx_product_name.addItem(ConstantsDB.MySQL_PRODUCT_NAME);        
    }
    private void common_action_cmbx_product_name_selected(){
        
        String product_name;
        String buff;
        
        buff=(String)this.cmbbx_product_name.getSelectedItem();        
        product_name=buff.toLowerCase();
        
        if(product_name.equals(ConstantsDB.MS_SQLSERVER_PRODUCT_NAME)){
            this.txt_port.setText(ConstantsDB.MS_SQLSERVER_PORT);
            this.txt_for_name_string.setText(ConstantsDB.MS_SQLSERVER_FOR_NAME_STRING);
        }
        if(product_name.equals(ConstantsDB.MySQL_PRODUCT_NAME)){
            this.txt_port.setText(ConstantsDB.MySQL_PORT);
            this.txt_for_name_string.setText(ConstantsDB.MySQL_FOR_NAME_STRING);
        }
        if((product_name.equals(ConstantsDB.MS_SQLSERVER_PRODUCT_NAME)||(product_name.equals(ConstantsDB.MySQL_PRODUCT_NAME)))){
            this.txt_user_name.requestFocus();
        }
        
    }

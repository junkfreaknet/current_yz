    public void valueChanged(javax.swing.event.TreeSelectionEvent e){
        
        javax.swing.tree.TreePath a_treepath=e.getNewLeadSelectionPath();
        javax.swing.tree.DefaultMutableTreeNode a_node;
        Object an_object;
        String obj_type="";
        
        System.out.println("");       
        System.out.println(new java.util.Date().toString());
        
        a_node=(javax.swing.tree.DefaultMutableTreeNode)a_treepath.getLastPathComponent();
        an_object=a_node.getUserObject();
        if(a_node.getUserObject() instanceof my.db.classes.core.Database){
            obj_type="database";
        }
        if(an_object instanceof my.db.classes.core.Table){
            obj_type="table";
        }
        if(an_object instanceof my.db.classes.core.Field){
            obj_type="field";
        }
        if(an_object instanceof my.db.classes.core.FieldLength){
            obj_type=a_node.getParent()+ " length";           
        }
        if(an_object instanceof my.db.classes.core.FieldType){
            obj_type=a_node.getParent()+" type";
        }
        System.out.println("select "+obj_type);
        
    }

    private JTree createTree(my.db.classes.common.ParaSQLServerMS para){
        
        //ParaSQLServerMS para=new ParaSQLServerMS();
        
        my.db.classes.core.Database db=new my.db.classes.core.Database(para);
        
        //db=my.db.utilities.Utilities.getPropertiesOfDB(para);        
        //System.out.println("num tables is " +db.getNumOfTables());        
        DefaultMutableTreeNode nodeDB=new DefaultMutableTreeNode(db);
        
        //tables begin
        for(int i_tables=Constants.CS_ZERO_AS_INT;i_tables<db.getNumOfTables();i_tables++){

            DefaultMutableTreeNode nodeTable=new DefaultMutableTreeNode(db.getTable(i_tables));
            //fields begin
            for(int i_fields=Constants.CS_ZERO_AS_INT;i_fields<db.getTable(i_tables).getNumOfFields();i_fields++){

                my.db.gui.tree.treenodes_old.Field nodeField=new my.db.gui.tree.treenodes_old.Field(db.getTable(i_tables).getField(i_fields));
                nodeTable.add((javax.swing.tree.DefaultMutableTreeNode)nodeField);
            }
            //fields end
            nodeDB.add(nodeTable);            
        }

        //tables end
        
        //finally
        DefaultTreeModel model=new DefaultTreeModel(nodeDB);
        
        return new JTree(model);
        
    }
